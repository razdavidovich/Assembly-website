document.addEventListener('DOMContentLoaded', () => {
  /* ==================
     Language Switching
     ================== */
  const langSwitcher = document.getElementById('langSwitcher');
  const spanEn = document.querySelector('.lang-en');
  const spanHe = document.querySelector('.lang-he');
  const htmlEl = document.documentElement;

  // Retrieve current lang or default to en
  let currentLang = localStorage.getItem('site_lang') || 'en';

  const applyLanguage = (lang) => {
    // 1. Swap dir attribute
    if (lang === 'he') {
      htmlEl.setAttribute('dir', 'rtl');
      htmlEl.setAttribute('lang', 'he');
      spanEn.classList.remove('active');
      spanHe.classList.add('active');
    } else {
      htmlEl.setAttribute('dir', 'ltr');
      htmlEl.setAttribute('lang', 'en');
      spanHe.classList.remove('active');
      spanEn.classList.add('active');
    }

    // 2. Loop through all tags with data-i18n
    const i18nElements = document.querySelectorAll('[data-i18n]');
    i18nElements.forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (translations[lang] && translations[lang][key]) {
        if (el.tagName.toLowerCase() === 'input' && el.type === 'text') {
           // We might map placeholder via code, but inputs uses general placeholders directly on DOM atm
           // If we supported placeholders natively: el.placeholder = translations[lang][key];
        } else {
           el.innerHTML = translations[lang][key]; 
        }
      }
    });

    localStorage.setItem('site_lang', lang);
  };

  // Initially apply language
  applyLanguage(currentLang);

  if (langSwitcher) {
    langSwitcher.addEventListener('click', () => {
      currentLang = currentLang === 'en' ? 'he' : 'en';
      applyLanguage(currentLang);
    });
  }


  /* ==================
     Mobile Menu Toggle
     ================== */
  const mobileToggle = document.querySelector('.mobile-toggle');
  const navLinks = document.querySelector('.nav-links');

  if (mobileToggle) {
    mobileToggle.addEventListener('click', () => {
      navLinks.classList.toggle('open');
      const isOpen = navLinks.classList.contains('open');
      mobileToggle.innerHTML = isOpen ? '&#10005;' : '&#9776;'; // Toggle icon
    });
  }


  /* ==================
     Smooth Scrolling
     ================== */
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
      e.preventDefault();
      
      // Close mobile menu if open
      if (navLinks.classList.contains('open')) {
        navLinks.classList.remove('open');
        mobileToggle.innerHTML = '&#9776;';
      }

      const targetId = this.getAttribute('href');
      if (targetId === '#') return;

      const targetElement = document.querySelector(targetId);
      if (targetElement) {
        window.scrollTo({
          top: targetElement.offsetTop - 80, // offset for navbar
          behavior: 'smooth'
        });
      }
    });
  });


  /* ==================
     Sticky Navbar Note
     ================== */
  const navbar = document.querySelector('.navbar');
  window.addEventListener('scroll', () => {
    if (window.scrollY > 50) {
      navbar.classList.add('scrolled');
    } else {
      navbar.classList.remove('scrolled');
    }
  });


  /* ==================
     Intersect Observers
     ================== */
  const fadeElements = document.querySelectorAll('.fade-in');
  const fadeObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, {
    threshold: 0.1,
    rootMargin: "0px 0px -50px 0px"
  });

  fadeElements.forEach(el => fadeObserver.observe(el));
});
